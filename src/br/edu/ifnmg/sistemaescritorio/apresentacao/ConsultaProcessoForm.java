    private void excluir() throws Exception {
        int linhaSelecionada = this.tblResultado.getSelectedRow();
            
        try { 
            Processo processoSelecionado = processos.get(linhaSelecionada);
            ProcessoBO processoBO = new ProcessoBO();
            
            String mensagem = "Realmente deseja excluir o processo "+processoSelecionado.getNumero()+"?";
                   
            int opcao = JOptionPane.showConfirmDialog(this,
                            mensagem,
                            "ExclusÃ£o de processo",
                            JOptionPane.YES_NO_OPTION);

            if (opcao == JOptionPane.YES_OPTION) {
                processoBO.excluirProcesso(processoSelecionado.getId());
                this.carregarTabelaProcesso();
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Ã necessÃ¡rio selecionar uma linha da tabela para possibilitar a exclusÃ£o de processo.",
                    "ExclusÃ£o de processo",
                    JOptionPane.INFORMATION_MESSAGE);
        }  

    }

